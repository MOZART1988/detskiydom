namespace app\components\widgets;

use app\modules\galleries\models\Galleries;
use app\modules\slides\models\Slides;
use yii\base\Widget;

class SliderWidget extends Widget
{
    public function run()
    {
        parent::run(); // TODO: Change the autogenerated stub

        if (\Yii::$app->controller->module->id !== 'basepage') {
            return false;
        }

        $slider = Slides::find()->where(['is_active' => 1])->limit(5)->all();

        if (!$slider) {
            return false;
        }

        return $this->render('sliderWidget', ['slider' => $slider]);
    }
}